#include "Point.hpp"

/* ************************************************************************** */
/* 							CONSTRUCTORS / DESTRUCTORS						  */
/* ************************************************************************** */

Point::Point() : _x(0), _y(0)
{
}

Point::Point(Point const &src)
{
	*this = src;
}

Point::Point(const float x, const float y) : _x(x), _y(y)
{
}

Point::~Point()
{
}

/* ************************************************************************** */
/* 									OPERATORS								  */
/* ************************************************************************** */

Point			&Point::operator=(Point const &rhs)
{
	if (this != &rhs)
	{
		(Fixed&)_x = rhs._x;
		(Fixed&)_y = rhs._y;
	}
	return (*this);
}

